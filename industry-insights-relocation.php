								<h1>Temporary Housing: Located at the Intersection of Globalization and Talent Mobility</h1>
								<img src="images/government/industry-insights/hero-industry-insights-relocation-02.jpg" alt="video-industry-insights" class="img-responsive" />
								<p class="caption">Photo Caption</p>
								<p>December 8, 2013 - As most mobility processionals know, the demand for global talent mobility has grown considerably over the last decade as increased connectivity and technology allows organizations to tap into more emerging markets and connect to talent around the globe in an instant. The increased demand for global staff means an organization’s mobility function has a significant role in supporting and driving company growth. For those involved in managing their organization’s global corporate housing program, ensuring alignment of business and mobility strategies is important for success.</p>
								<p>Mobility professionals continue to look for ways to keep down costs for global assignments and they must consider how to balance this against offering a program that is robust enough to attract and retain talent. If organizations fail to make the proper investment into the program, including training, mentoring and assimilation programs, along with a clear plan for career success, then assignees can become disenchanted and less productive, which is also costly. While most organizations help assignees with relocation, they may need to consider how securing the right temporary accommodations will help the assignee settle in and become productive more quickly.</p>
								<p>According to a 2012 Cartus survey of international mobility professionals, outside of controlling costs, complying with laws and regulations, security concerns and housing were the greatest challenges they face. They are finding housing to be costly and in short supply in established markets. Moreover, in emerging markets, housing is inconsistent and may not meet minimum standards. Increasingly, talent mobility managers are seeing that they get the best value by placing assignees in corporate or serviced apartments. </p>
								<p>Data from Oakwood Worldwide indicates that since 2000, the demand for global corporate and serviced apartments is steadily rising. Cities like Singapore, Toronto and London now represent top global markets and Oakwood Worldwide has seen an increase in serviced apartment placements in emerging markets with EMEA and APAC regions leading the number of placements in 2013. Navigating the often-complicated landscape of global corporate housing markets requires mobility professionals to work in partnership with experienced corporate and serviced apartment providers. Providers who understand the latest global housing trends and prepare for these challenges can design scalable, flexible solutions that work best for each customer.</p>
								<p>Some of the global trends affecting housing exist in response to economic conditions from the past five years. Supply is unusually tight as plans for new construction projects have been delayed or abandoned. Given this dynamic, apartment suppliers are enjoying greater demand from standard renters – making access to the short-term leases needed by mobility professionals more difficult to access. This situation is also giving suppliers room to increase rents, a trend that is taking place in nearly all regions of the globe.</p>
								<p>In emerging markets, providers also have to contend with variances in the types of housing options they can offer. From number of bedrooms to available amenities, providers must provide several housing options that meet expectations that are often based on Western standards. For the mobility professional, being able to obtain a deep understanding of the issues in each market and set expectations for assignees is an important part of the process.  Mobility professionals are looking for providers who understand their challenges and can deliver cost-effective solutions for their organizations, while keeping assignees satisfied and productive.</p>
								<p>Knowledgeable providers, like Oakwood Worldwide, are preparing their customers to respond to these global mobility trends by helping them create a global corporate housing program focused on securing the right apartment options at the right time. </p>
								<p>For more information about how to build a successful global corporate housing program for your organization, visit <a href="#">www.oakwoodworldwide.com/relocation</a> and check out the next article in our monthly series: Legal & Regulatory Considerations for Global Housing Programs. </p>
